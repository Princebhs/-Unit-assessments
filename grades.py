mark = int(input("Enter student grade: "))

if  mark >= 70 and mark <=100:
    print("A")

elif mark >= 60 and mark <= 69:
    print("B")

elif mark >= 50 and mark <= 59:
    print("C")

elif mark >= 40 and mark <= 49:
    print("D")

elif mark >= 30 and mark <= 39:
    print("E")

else:
    print("F")
    
